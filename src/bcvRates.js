/**
 * Retorna la tasa BCV histórica para un mes dado.
 * Si el mes coincide con el mes de egreso, usa la tasa actual (más precisa).
 */
function getTasaMes(anio, mes, mesEgresoKey, tasaActual) {
  const key = `${anio}-${String(mes).padStart(2, '0')}`;
  if (key === mesEgresoKey) return tasaActual;
  return TASAS_HISTORICAS[key] ?? tasaActual;
}

/**
 * Devuelve la tasa BCV del mes de egreso desde la tabla histórica.
 * @param {string} fechaEgresoNorm  DD/MM/AAAA
 * @returns {number|null}
 */
function tasaDelMesEgreso(fechaEgresoNorm) {
  const p = String(fechaEgresoNorm ?? '').split('/');
  if (p.length < 3) return null;
  const key = `${p[2]}-${p[1].padStart(2, '0')}`;
  return TASAS_HISTORICAS[key] ?? null;
}

